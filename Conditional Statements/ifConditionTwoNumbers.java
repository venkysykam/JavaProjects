public class ifConditionTwoNumbers {
    public static void main(String[] args) {
        int a = 1;
        int b = 1;
        if (a > b) {
            System.out.println("a is greater");
        } else if (b > a) {
            System.out.println("b is greater");
        } else
            System.out.println("a and b are equal");
    }
}
